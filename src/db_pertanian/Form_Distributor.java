    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        try {
            if (txtID.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Data Tidak Boleh Kosong");
            } else {
            String sql = "INSERT INTO tb_distributor VALUES ('"+txtID.getText()+"','"+txtNama.getText()+"','"
                    +txtAlamat.getText()+"','"+txtHP.getText()+"')";
            java.sql.Connection con=(Connection) Koneksi2.koneksi();
            java.sql.PreparedStatement pat=con.prepareStatement(sql);
            pat.execute();
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Gagal Menyimpan Data");
        }
        load_table();
        refresh();
        txtID.requestFocus();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        try{
            String sql = "update tb_distributor set Nama = '"+txtNama.getText()+"' ,Alamat='"+txtAlamat.getText()+
                    "' ,No_HP='"+txtHP.getText()+"' where ID_Distributor ='"+txtID.getText()+"'";
            java.sql.Connection conn = (Connection)Koneksi2.koneksi();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Edit Data Berhasil");
            btnRefreshActionPerformed(evt);
        }catch (HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(this, e.getMessage());
    }   load_table(); 
    }//GEN-LAST:event_btnUbahActionPerformed

