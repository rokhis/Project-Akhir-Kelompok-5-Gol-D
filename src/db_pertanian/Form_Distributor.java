    private void tbDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataMouseClicked
        // TODO add your handling code here:
        int baris = tbData.rowAtPoint(evt.getPoint());
        String id = tbData.getValueAt(baris,1).toString();
        txtID.setText(id);
        String nama = tbData.getValueAt(baris,2).toString();
        txtNama.setText(nama);
        String alamat = tbData.getValueAt(baris,3).toString();
        txtAlamat.setText(alamat);
        String hp = tbData.getValueAt(baris,4).toString();
        txtHP.setText(hp);
    }//GEN-LAST:event_tbDataMouseClicked

